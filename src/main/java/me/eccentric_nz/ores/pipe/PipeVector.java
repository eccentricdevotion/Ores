package me.eccentric_nz.ores.pipe;

public class PipeVector {

    private int x;
    private int y;
    private int z;

    public PipeVector(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}
